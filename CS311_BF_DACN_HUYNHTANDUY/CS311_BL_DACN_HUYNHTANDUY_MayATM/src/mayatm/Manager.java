    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        StringBuilder sb = new StringBuilder();
        if(txtName.getText().equals("") || txtUserName.getText().equals("")||txtPass.getText().equals("")
                || txtCardNumber.getText().equals("")||txtMoney.getText().equals("")
                ) sb.append("Bạn chưa nhập đủ thông tin");
        if(sb.length()>0){
            JOptionPane.showMessageDialog(this, sb.toString(),"Error",JOptionPane.ERROR_MESSAGE);
            return ;
        }
        String str = "Thêm người dùng mới !";
        boolean check = false;
        for(User x: a){
            if(x.getCmnd().equals(txtCmnd.getText()) || clickUpdateButton==true){
                str="Cập nhật lại thông tin !";
                check = true;
                clickUpdateButton=false;
            }
        }
        int choice = JOptionPane.showConfirmDialog(this, str,str,JOptionPane.YES_NO_OPTION);
        if(choice==JOptionPane.NO_OPTION){
            return;
        }

        User user = new User(txtName.getText(), txtCmnd.getText()
                ,txtUserName.getText(), txtPass.getText()
                ,cbxBankName.getSelectedItem().toString(),txtCardNumber.getText()
                ,Long.parseLong(txtMoney.getText()));
        if(check==false) {
            for(User z: a){
                if(user.getUserName().equals(z.getUserName())){
                    JOptionPane.showMessageDialog(this, "Username đã tồn tại","Error",JOptionPane.ERROR_MESSAGE);
                    break;
                }
            }
            a.add(user);
            ghiFile(a);
            fillTable();
        }
        else
            for(User x: a){
            if(x.getCmnd().equals(txtCmnd.getText())){
                x.setName(txtName.getText());
                x.setUserName(txtUserName.getText());
                x.setPass(txtPass.getText());
                x.setMoney(Long.parseLong(txtMoney.getText()));
                x.setCardNumber(txtCardNumber.getText());
                x.setBankName(cbxBankName.getSelectedItem().toString());
                fillTable();
                ghiFile(a);
            }
        }
        setLableEdit(false);
    }//GEN-LAST:event_btnSaveActionPerformed
