    private void fillTable(){
        tblModel.setRowCount(0);
        for(User user: a){
            tblModel.addRow(new Object[]{user.getCmnd(),user.getUserName()
                    ,user.getPass(),user.getName(),user.getBankName(),
            user.getCardNumber(),user.getMoney()});
        }
        tblModel.fireTableDataChanged();
    }
